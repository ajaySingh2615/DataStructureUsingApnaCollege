package com.operators;

public class Main {
    public static void main(String[] args) {
        // Symbols that tell compiler to perform some operators
        // sum = a + b

        // Arithmetic Operator
        // Relational Operator
        // Logical Operator
        // Assignment Operator


        // Unary Operator
        // ++
        // Pre Increment ++a
        int a = 10;
        int b = ++a;
        System.out.println("a = " + a);
        System.out.println("b = " + b);

        // Post Increment a++
        int aa = 10;
        int bb = aa++;
        System.out.println("aa = " + aa);
        System.out.println("bb = " + bb);
    }
}